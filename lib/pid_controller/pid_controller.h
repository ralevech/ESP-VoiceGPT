// ====================================================================
// pid_controller.h - Класс PID регулятора
// 
// Описание: Реализует стандартный PID регулятор с дискретным временем.
//           Формула: output = Kp*error + Ki*integral + Kd*derivative
// Поддерживает:
//           - Антивиндовап (ограничение интегральной составляющей)
//           - Ограничение выходного сигнала
//           - Сброс интегратора
// Автор: ralevech
// ====================================================================

#ifndef PID_CONTROLLER_H
#define PID_CONTROLLER_H

#include <Arduino.h>

class PIDController {
public:
    // -----------------------------------------------------------------
    // Конструктор
    // dt - период дискретизации (секунды)
    // -----------------------------------------------------------------
    PIDController(float dt = 0.1);
    
    // -----------------------------------------------------------------
    // Установка коэффициентов PID
    // -----------------------------------------------------------------
    void setGains(float Kp, float Ki, float Kd);
    
    // -----------------------------------------------------------------
    // Установка лимитов интегральной составляющей (антивиндовап)
    // -----------------------------------------------------------------
    void setIntegralLimits(float min, float max);
    
    // -----------------------------------------------------------------
    // Установка лимитов выходного сигнала
    // -----------------------------------------------------------------
    void setOutputLimits(float min, float max);
    
    // -----------------------------------------------------------------
    // Сброс интегральной составляющей
    // -----------------------------------------------------------------
    void resetIntegral();
    
    // -----------------------------------------------------------------
    // Сброс всех внутренних состояний (интеграл, прошлая ошибка)
    // -----------------------------------------------------------------
    void reset();
    
    // -----------------------------------------------------------------
    // Основной расчёт PID
    // target  - целевое значение (уставка)
    // current - текущее значение (обратная связь)
    // Возвращает управляющий сигнал
    // -----------------------------------------------------------------
    float compute(float target, float current);
    
    // -----------------------------------------------------------------
    // Получение текущих значений (для отладки)
    // -----------------------------------------------------------------
    float getError() const { return m_error; }
    float getIntegral() const { return m_integral; }
    float getDerivative() const { return m_derivative; }
    float getOutput() const { return m_output; }
    
    // -----------------------------------------------------------------
    // Изменение периода дискретизации на лету
    // -----------------------------------------------------------------
    void setDt(float dt);

private:
    float m_dt;                 // Период дискретизации (сек)
    float m_Kp;                 // Пропорциональный коэффициент
    float m_Ki;                 // Интегральный коэффициент
    float m_Kd;                 // Дифференциальный коэффициент
    
    float m_integralMin;        // Минимум интегральной составляющей
    float m_integralMax;        // Максимум интегральной составляющей
    float m_outputMin;          // Минимум выходного сигнала
    float m_outputMax;          // Максимум выходного сигнала
    
    float m_error;              // Текущая ошибка
    float m_previousError;      // Предыдущая ошибка (для производной)
    float m_integral;           // Накопленная интегральная ошибка
    float m_derivative;         // Производная ошибки
    float m_output;             // Выходной сигнал
};

#endif // PID_CONTROLLER_H