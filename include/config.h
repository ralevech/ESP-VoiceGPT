#ifndef CONFIG_H
#define CONFIG_H

// ====================================================================
// WiFi НАСТРОЙКИ (клиентский режим)
// ====================================================================
#define WIFI_SSID     "Gorizont_2"      // Имя WiFi сети
#define WIFI_PASS     "31123522"        // Пароль WiFi сети

// ====================================================================
// ТОЧКА ДОСТУПА (режим AP) — используется если WiFi не подключен
// ====================================================================
#define AP_SSID       "ESP32_AP"        // Имя точки доступа ESP32
#define AP_PASS       "12345678"        // Пароль точки доступа (мин. 8 символов)

// ====================================================================
// ПАРАМЕТРЫ ПОДКЛЮЧЕНИЯ К WIFI
// ====================================================================
#define MAX_ATTEMPTS      3             // Максимальное число попыток
#define ATTEMPT_TIMEOUT_MS  10000       // Таймаут одной попытки (мс)

// ====================================================================
// ПИНЫ ВВОДА/ВЫВОДА
// ====================================================================
#define LED_PIN       LED_BUILTIN       // Встроенный светодиод (не используется с RGB)
#define RGB_LED_PIN   48                // Пин для RGB светодиода WS2812 (ESP32-S3)
#define NUM_LEDS      1                 // Количество светодиодов в ленте/кольце

// ====================================================================
// ПРИОРИТЕТЫ ЗАДАЧ FreeRTOS (диапазон 0-24, где 0 — низший)
// ====================================================================
#define PRIO_SERIAL   7                 // Задача Serial отладки
#define PRIO_WIFI     5                 // Задача управления WiFi (высший приоритет)
#define PRIO_BLINK    3                 // Задача мигания светодиодом
#define PRIO_SERVER   2                 // Веб-сервер
#define PRIO_SENSOR   1                 // Опрос датчиков (низший)

// ====================================================================
// РАЗМЕР СТЕКА ЗАДАЧ (в байтах)
// ====================================================================
#define STACK_WIFI    4096              // WiFi задача
#define STACK_BLINK   2048              // Мигание светодиодом
#define STACK_SERIAL  2048              // Serial отладка
#define STACK_SERVER  8192              // Веб-сервер (требует больше памяти)
#define STACK_SENSOR  4096              // Датчики

// ====================================================================
// ПРИВЯЗКА ЗАДАЧ К ЯДРАМ ПРОЦЕССОРА (ESP32-S3 имеет 2 ядра)
// ====================================================================
#define CORE_WIFI     0                 // Ядро 0 (обычно используется для WiFi)
#define CORE_BLINK    1                 // Ядро 1 (для задач, не связанных с WiFi)
#define CORE_SERIAL   0                 // Ядро 0 (отладка не требует отдельного ядра)
#define CORE_SERVER   0                 // Ядро 0 (веб-сервер рядом с WiFi для быстрого доступа)
#define CORE_SENSOR   1                 // Ядро 1 (опрос датчиков параллельно с основными задачами)

// ====================================================================
// ТАЙМИНГИ ЗАДАЧ (в миллисекундах)
// ====================================================================
#define DELAY_WIFI_CHECK    10000       // Проверка состояния WiFi (10 сек)
#define DELAY_BLINK         500         // Базовая задержка мигания (0.5 сек)
#define DELAY_SERIAL_PRINT  5000        // Вывод отладочной информации (5 сек)
#define DELAY_SENSOR_CHECK  2000        // Опрос датчиков (2 сек)

// ====================================================================
// НАСТРОЙКИ WATCHDOG (сторожевой таймер)
// ====================================================================
#define ENABLE_WATCHDOG     1           // 1 = включить, 0 = выключить сторожа во всех задачах
#define WATCHDOG_TIMEOUT_MS 10000       // Таймаут в миллисекундах (10 секунд)
                                        // Если задача не сбросит watchdog за это время,
                                        // ESP32 автоматически перезагрузится

#endif // CONFIG_H