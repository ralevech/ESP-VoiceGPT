#include <Arduino.h>
#include <esp_task_wdt.h>
#include "common.h"
#include "globals.h"
#include "config.h"

// ====================================================================
// toggleLED() - Переключение встроенного светодиода
// ====================================================================
void toggleLED() {
    ledState = !ledState;
    digitalWrite(LED_PIN, ledState ? HIGH : LOW);
}

// ====================================================================
// feedWatchdog() - Сброс сторожевого таймера
// ====================================================================
void feedWatchdog() {
#if ENABLE_WATCHDOG
    esp_task_wdt_reset();
#endif
}