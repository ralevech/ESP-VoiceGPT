#include <Arduino.h>
#include "config.h"
#include "common.h"
#include "audio_priv.h"

#if ENABLE_AUDIO

// --------------------------------------------------------------------
// audio_info() - Вызывается библиотекой при информационных событиях
// --------------------------------------------------------------------
void audio_info(const char* info) {
    Serial.println("[DEBUG] " + String(info));
}

// --------------------------------------------------------------------
// audio_id3data() - Вызывается при получении метаданных MP3 (название, исполнитель)
// --------------------------------------------------------------------
void audio_id3data(const char* info) {
    Serial.println("[DEBUG] ID3: " + String(info));
}

// --------------------------------------------------------------------
// audio_eof_stream() - Вызывается при завершении воспроизведения (конец файла/потока)
// --------------------------------------------------------------------
void audio_eof_stream(const char* info) {
    Serial.println("[DEBUG] End of stream: " + String(info));
    isPlaying = false;
    currentFile = "";
}

#endif