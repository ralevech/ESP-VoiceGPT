// ====================================================================
// task_blink.cpp - Задача управления RGB светодиодом (эффект радуги)
// 
// Описание: Бесконечная задача FreeRTOS, реализующая плавное
//           переливание цветов RGB светодиода WS2812
// Автор: ralevech
// ====================================================================

#include <Arduino.h>
#include <Adafruit_NeoPixel.h>
#include "config.h"
#include "common.h"

// --------------------------------------------------------------------
// Глобальный объект для работы с RGB светодиодом
// Параметры: количество светодиодов, пин, формат данных
// --------------------------------------------------------------------
Adafruit_NeoPixel pixels(NUM_LEDS, RGB_LED_PIN, NEO_GRB + NEO_KHZ800);

// Прототип вспомогательной функции для генерации радужного спектра
uint32_t Wheel(byte wheelPos);

// ====================================================================
// taskBlink() - Основная задача мигания светодиодом
// 
// Алгоритм:
//   1. Инициализация светодиода (pinMode, яркость)
//   2. Бесконечный цикл с плавным перебором 256 оттенков
//   3. Задержка 10 мс между сменами цвета для плавности
//   4. Периодический сброс watchdog
// ====================================================================
void taskBlink(void *pvParameters) {
    (void)pvParameters;
    
    // ---- ИНИЦИАЛИЗАЦИЯ -------------------------------------------------
    pixels.begin();                    // Инициализация пина и таймингов
    pixels.setBrightness(50);          // Яркость 50% (0-255, не слепит)
    pixels.clear();                    // Сброс цвета всех светодиодов
    pixels.show();                     // Отправка команды (ВАЖНО!)
    
    // ---- ОСНОВНОЙ ЦИКЛ ЗАДАЧИ -----------------------------------------
    while(true) {
        // Плавное переливание цветов от 0 до 255 (полный круг радуги)
        for(int i = 0; i < 256; i++) {
            // Установка цвета для единственного светодиода (индекс 0)
            pixels.setPixelColor(0, Wheel(i));
            pixels.show();             // Применить изменения
            vTaskDelay(pdMS_TO_TICKS(10));  // Пауза 10 мс для плавности
            
            // Сброс watchdog (задача жива)
            feedWatchdog();
        }
    }
}

// ====================================================================
// Wheel() - Преобразование позиции в цвет радуги
// 
// Вход:   wheelPos - позиция на цветовом круге (0-255)
// Выход:  цвет в формате GRB для библиотеки Adafruit_NeoPixel
// 
// Секторы:
//   0-84:   Красный → Зеленый
//   85-169: Зеленый → Синий
//   170-254: Синий → Красный
// ====================================================================
uint32_t Wheel(byte wheelPos) {
    wheelPos = 255 - wheelPos;         // Инверсия направления
    
    if(wheelPos < 85) {
        // Сектор 1: от Красного к Зеленому
        return pixels.Color(255 - wheelPos * 3, 0, wheelPos * 3);
    }
    else if(wheelPos < 170) {
        // Сектор 2: от Зеленого к Синему
        wheelPos -= 85;
        return pixels.Color(0, wheelPos * 3, 255 - wheelPos * 3);
    }
    else {
        // Сектор 3: от Синего к Красному
        wheelPos -= 170;
        return pixels.Color(wheelPos * 3, 255 - wheelPos * 3, 0);
    }
}